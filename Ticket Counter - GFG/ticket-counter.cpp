class Solution {
  public:
    int distributeTicket(int n, int k) {
        // code here
       int sp = 1;
       int ep = n;
       while(ep - sp > k){
           sp = sp+k;
           ep = ep - k;
         
       }
       return ep<sp?sp:ep;
    }
};


